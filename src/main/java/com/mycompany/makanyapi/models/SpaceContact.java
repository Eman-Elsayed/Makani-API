package com.mycompany.makanyapi.models;

import com.fasterxml.jackson.annotation.JsonIgnore;
import java.io.Serializable;
import javax.persistence.Basic;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.JoinColumn;
import javax.persistence.ManyToOne;
import javax.persistence.NamedQueries;
import javax.persistence.NamedQuery;
import javax.persistence.Table;
import javax.validation.constraints.Size;
import javax.xml.bind.annotation.XmlRootElement;

/**
 *
 * @author Ahmed
 */
@Entity
@Table(name = "space_contact")
@XmlRootElement
@NamedQueries({
    @NamedQuery(name = "SpaceContact.findAll", query = "SELECT s FROM SpaceContact s"),
    @NamedQuery(name = "SpaceContact.findById", query = "SELECT s FROM SpaceContact s WHERE s.id = :id"),
    @NamedQuery(name = "SpaceContact.findByTelephone", query = "SELECT s FROM SpaceContact s WHERE s.telephone = :telephone")})
public class SpaceContact implements Serializable {
    private static final long serialVersionUID = 1L;
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Basic(optional = false)
    @Column(name = "id")
    private Integer id;
    @Size(max = 45)
    @Column(name = "telephone")
    private String telephone;
    @JoinColumn(name = "space_id", referencedColumnName = "id")
    @ManyToOne
    @JsonIgnore
    private Space spaceId;

    public SpaceContact() {
    }

    public SpaceContact(Integer id) {
        this.id = id;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getTelephone() {
        return telephone;
    }

    public void setTelephone(String telephone) {
        this.telephone = telephone;
    }

    public Space getSpaceId() {
        return spaceId;
    }

    public void setSpaceId(Space spaceId) {
        this.spaceId = spaceId;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof SpaceContact)) {
            return false;
        }
        SpaceContact other = (SpaceContact) object;
        if ((this.id == null && other.id != null) || (this.id != null && !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "com.mycompany.makanyapi.models.SpaceContact[ id=" + id + " ]";
    }
    
}
